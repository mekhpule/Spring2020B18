package day18_NestedLoop;

public class NestedLoop {

    public static void main(String[] args) {

        for (int j = 0; j < 5; j++) {

            for (int i = 1; i <= 5; i++) { // 1, 2, 3, 4, 5

                System.out.print(i);

            }

            System.out.println();

        }
    }
}
